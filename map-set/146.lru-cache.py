import collections

# 哈希表来实现O(1)查询
# 双向链表维护插入顺序，哈希表的key存在链表中


class LRUCache:

    def __init__(self, capacity: int):
        # 有序字典，按照插入的顺序排序
        self.dic = collections.OrderedDict()
        self.remain = capacity

    def get(self, key: int) -> int:
        if key not in self.dic:
            return -1
        v = self.dic.pop(key)
        # 相当于再插入这个元素，使其成为第一个元素
        self.dic[key] = v
        return v

    def put(self, key: int, value: int) -> None:
        if key in self.dic:
            self.dic.pop(key)
        else:
            if self.remain > 0:
                self.remain -= 1
            else:
                # 弹出最早插入的元素
                self.dic.popitem(last=False)
        # 把当前元素插入
        self.dic[key] = value


